# check if this file is a afm file.
def ifAFM(file)
  f = IO.readlines(file)
  if f[0] =~ /StartFontMetrics/
    return true
  else
    return false
  end
end

# turn to real character
def uni(s)
  if s[0,3] == 'uni'
    s.sub(/uni(\w+)/){[$1.hex].pack('U*')}
  elsif s[0,1] == 'u' && s[0,3] != 'uni'
    s.sub(/u(\w+)/){[$1.hex].pack('U*')}
  else
    s.sub(/(\w+)/){[$1.hex].pack('U*')}
  end
end

# check if in agl
def isagl(s)
  if AGL.key?(s)
    return true
  else
    return false
  end
end

def mkhtf(file)
  m = 0; a = []
  base = File.basename(file, File.extname(file))
  htf = base + ".htf"
  open(file).each do |line|
    one = line.split[7]
    two =line.split[1]
    if ARGV[0] == '-c' && line =~ /^C / && line !~ /^C -1/
      if line =~ /nounicode/
        a << "'' '' " + two
      elsif isagl(one)
        a << "'" + uni(AGL[one]) + "' '' " + two
      else
        a << "'" + uni(one) + "' '' " + two
      end
    elsif line =~ /^C / && line !~ /^C -1/
      if line =~ /nounicode/
        a << "'' '' " + two
      elsif isagl(one)
        a << "'&#x" + AGL[one] + ";' '' " + two
      else
        a << one.sub(/uni|u/,"'&#x") + ";' '' " + two
      end
    end
  end
  be = a.first.split[2]
  en = a.last.split[2]
  num = a.size
  f = open(htf,'w')
  f.puts base + " " + be + " " + en
  f.puts a[m] + " % mkhtf.rb v#{VER}, #{T} Edward G.J. Lee."
  while m < num -1
    gap = a[m+1].split[2].to_i - a[m].split[2].to_i
    if gap == 1
      f.puts a[m+1]
    else
      (gap-1).times{f.puts "'' '' ''"}
      f.puts a[m+1]
    end
    m += 1
  end
  f.puts base + " " + be + " " + en
  f.close
end

def usage()
  puts "#{PNAME} Version #{VER}"
  puts "Usage:"
  puts "  #{PNAME} your.afm    => use UCS codepoint"
  puts "  #{PNAME} -c your.afm => use UTF-8 character"
end

if ARGV.length == 2
  if ARGV[0] == '-c' && !File.exist?(ARGV[1])
    puts 'File does not exist!'
    exit
  elsif ARGV[0] == '-c' && !ifAFM(ARGV[1])
    puts 'Not a afm file!'
    exit
  elsif ARGV[0] == '-c' && ifAFM(ARGV[1])
    mkhtf(ARGV[1])
  else
    usage()
    exit
  end
else
  if ARGV.length != 1
    usage()
    exit
  elsif !File.exist?(ARGV[0])
    puts 'File does not exist!'
    exit
  elsif !ifAFM(ARGV[0])
    puts 'Not a afm file!'
    exit
  else
    mkhtf(ARGV[0])
  end
end
